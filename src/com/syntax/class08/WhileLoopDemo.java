package com.syntax.class08;

public class WhileLoopDemo {
	public static void main(String[] args) {
		int num=1;
		while(num<=10) {
			System.out.println("Welcome");
			num++;
		}
		
		int num1=1;
		while(num1<=30) {
			if(num1==10) {
				System.out.println("Hey I'm tenth iteration");
			}
			num1++;
		}
		
	}

}
