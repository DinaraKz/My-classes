package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main (String [] args) {
		/* * Primitive Data Types
		 * 
		 * byte
		 * short
		 * int
		 * long
		 * float
		 * double
		 * char
		 * boolean
		 */
		
		// Whole 
		
		byte num = 10;
		
		short num1 = 100;
		
		int num2 = 1000;
		
		// l or L
		long num3 = 1000000000756164L;
		
		// Decimal Number
		
		// f or F
		float num4 = 12.9F;
		
		double num5 = 0.888;
		
		// Non-Number Data Types
		
		char add = '+';
		char letter = 'a';
		
		//true or false (Yes or No)
		// do not put Yes or No for value
		boolean hunry = true;
		boolean thirsty = false;
		
	}
}
